/**
 * @param {string} subsetKey — код subset Google
 * @returns {string} пусто, если блока нет
 */
export function scriptUnicodeGlyphSample(subsetKey) {
  const key = String(subsetKey || '').toLowerCase();
  const ranges = SCRIPT_UNICODE_BLOCKS[key];
  if (!ranges) return '';
  return joinRanges(ranges);
}
