/**
 * Строки «как латинский полный набор», собранные из диапазонов Unicode (превью шрифтов).
 * Ключи — как в metadata Google (нижний регистр, дефисы).
 * Для &text= в URL Google лучше оставлять короткие сниппеты в googleFontCatalogSampleText.
 */

function joinRanges(ranges) {
  let out = '';
  for (const [a, b] of ranges) {
    for (let cp = a; cp <= b; cp += 1) {
      out += String.fromCodePoint(cp);
    }
  }
  return out;
}

